Conditionals

if (age > 18) {
    console.log("Adult");
} else {
    console.log("Minor");
}

switch (day) {
    case "Monday":
        console.log("Start of the week");
        break;
    default:
        console.log("Another day");
}

Loops

for (let i = 0; i < 5; i++) {
    console.log(i);
}

while (count < 10) {
    count++;
}

do {
    console.log("Runs at least once");
} while (false);
